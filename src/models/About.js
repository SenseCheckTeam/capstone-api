const mongoose = require('mongoose');
const { Schema } = mongoose;

const aboutSchema = new Schema({
  id: {
    type: String,
    required: true,
    unique: true,
  },
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  imageUrl: {
    type: String,
    required: true,
  },
  textButton: {
    type: String,
    required: true,
  },
  buttonUrl: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

const About = mongoose.model('About', aboutSchema);

module.exports = About;
